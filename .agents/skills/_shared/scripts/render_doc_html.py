MARKED_CDN_TAG = '<script defer src="https://cdn.jsdelivr.net/npm/marked@14/marked.min.js"></script>'
# marked 优先内联（离线可用、消灭"CDN 失败整页糊 Markdown 原文"）；vendor 缺失时退回 CDN
VENDOR_MARKED = Path(__file__).resolve().parent.parent / "vendor" / "marked.min.js"

_STRONG_RE = re.compile(r"\*\*([^*\n]+?)\*\*")
_FENCE_RE = re.compile(r"^\s{0,3}(```|~~~)")


def fix_cjk_strong(body: str) -> str:
    """构建期把 **…** 预转 <strong>。

    CommonMark 的强调定界规则与中文标点冲突（如 `**…。**` 后接汉字时闭合失效），
    浏览器端 marked 会露出字面星号或加粗渗漏到后文；在 Python 侧确定性转换绕开该坑。
    跳过 code fence 与行内 code span（按反引号奇偶段判断）。
    """
    out_lines = []
    in_fence = False
    for line in body.split("\n"):
        if _FENCE_RE.match(line):
            in_fence = not in_fence
            out_lines.append(line)
            continue
        if in_fence or "**" not in line:
            out_lines.append(line)
            continue
        parts = line.split("`")
        for i in range(0, len(parts), 2):
            parts[i] = _STRONG_RE.sub(r"<strong>\1</strong>", parts[i])
        out_lines.append("`".join(parts))
    return "\n".join(out_lines)


def marked_script_tag() -> str:
    if VENDOR_MARKED.is_file():
        js = VENDOR_MARKED.read_text(encoding="utf-8").replace("</script", "<\\/script")
        return "<script>" + js + "</script>"
    return MARKED_CDN_TAG

