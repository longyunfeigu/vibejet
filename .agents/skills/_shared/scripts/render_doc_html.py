def split_frontmatter(text: str) -> tuple[str, str]:
    """返回 (frontmatter 原文不含围栏, 正文)。无 frontmatter 时前者为空串。"""
    if not text.startswith("---\n") and not text.startswith("---\r\n"):
        return "", text
    lines = text.splitlines(keepends=True)
    for i in range(1, len(lines)):
        if lines[i].strip() == "---":
            return "".join(lines[1:i]), "".join(lines[i + 1 :])
    return "", text


def parse_meta(frontmatter: str, body: str, md_path: Path) -> dict:
    """从 frontmatter + 内容特征提取文档头横幅用的元信息。"""
    meta: dict[str, str] = {}
    for line in frontmatter.splitlines():
        m = re.match(r"^([A-Za-z_]+):\s*[\"']?([^\"']+?)[\"']?\s*$", line)
        if m:
            meta[m.group(1)] = m.group(2)
    kind = meta.get("type", "")
    if not kind:
        if meta.get("epic_id") and meta.get("epic_name"):
            kind = "epic"
        elif re.match(r"^us\d{3}-", md_path.name) or re.search(r"^### Story \d+\.\d+", body, re.M):
            kind = "story"
        elif "EARS" in body[:2000]:
            kind = "prd"
    meta["_kind"] = kind
    meta["_kind_label"] = DOC_KIND_LABEL.get(kind, "")
    return meta


def build_banner(meta: dict, title: str) -> str:
    """plan/epic/story 族生成文档头横幅；识别不出家族时返回空串（零影响）。"""
    if not meta.get("_kind_label"):
        return ""
    chips: list[str] = []
    if meta.get("epic_id"):
        chips.append(f'<span class="chip meta">Epic {html.escape(meta["epic_id"])}</span>')
    policy = meta.get("execution_policy", "")
    if policy:
        cls = "pol-strict" if "strict" in policy else "pol-fast"
        chips.append(f'<span class="chip {cls}">{html.escape(policy)}</span>')
    if meta.get("status"):
        chips.append(f'<span class="chip doc-status">{html.escape(meta["status"])}</span>')
    if meta.get("priority"):
        chips.append(f'<span class="chip meta">{html.escape(meta["priority"])}</span>')
    if meta.get("date"):
        chips.append(f'<span class="chip meta">{html.escape(meta["date"])}</span>')
    meta_row = f'<div class="meta-row">{"".join(chips)}</div>' if chips else ""
    return (
        '<header class="doc-banner">'
        f'<div class="kicker">{html.escape(meta["_kind_label"])}</div>'
        f'<div style="font-size:1.05rem;font-weight:600">{html.escape(title)}</div>'
        f"{meta_row}</header>"
    )


def extract_title(body: str, fallback: str) -> str:
    """取正文第一个 H1 作为页面标题；跳过 code fence 内的 '# ' 行。"""
    in_fence = False
    for line in body.splitlines():
        if line.lstrip().startswith("```"):
            in_fence = not in_fence
            continue
        if not in_fence and line.startswith("# "):
            return line[2:].strip()
    return fallback


def render_file(md_path: Path) -> Path:
    text = md_path.read_text(encoding="utf-8")
    frontmatter, body = split_frontmatter(text)
    body = fix_cjk_strong(body)
    title = extract_title(body, md_path.stem)
    meta = parse_meta(frontmatter, body, md_path)

    frontmatter_block = ""
    if frontmatter.strip():
        frontmatter_block = (
            '<details class="frontmatter"><summary>文档元数据（frontmatter）</summary>'
            f"<pre>{html.escape(frontmatter.strip())}</pre></details>"
        )

    # < 转义防正文中的 </script> / <!-- 破坏内联 script；U+2028/2029 是旧 JS 引擎的行终止符
    def js_json(value) -> str:
        return (
            json.dumps(value, ensure_ascii=False)
            .replace("<", "\\u003c")
            .replace("\u2028", "\\u2028")
            .replace("\u2029", "\\u2029")
        )

    out_path = md_path.with_suffix(".html")
    out_path.write_text(
        PAGE_TEMPLATE.substitute(
            title=html.escape(title),
            source_name=html.escape(md_path.name),
            banner_block=build_banner(meta, title),
            frontmatter_block=frontmatter_block,
            marked_script_tag=marked_script_tag(),
            mermaid_script_tag=MERMAID_SCRIPT_TAG if "```mermaid" in body else "",
            md_json=js_json(body),
            doc_meta_json=js_json(meta),
        ),
        encoding="utf-8",
    )
    return out_path


def collect_md_files(args: list[str]) -> list[Path]:
    files: list[Path] = []
    for arg in args:
        p = Path(arg)
        if p.is_dir():
            files.extend(sorted(p.rglob("*.md")))
        elif p.is_file() and p.suffix == ".md":
            files.append(p)
        else:
            print(f"error: 不是 .md 文件或目录: {arg}", file=sys.stderr)
            sys.exit(1)
    return files


def main() -> None:
    if len(sys.argv) < 2:
        print(__doc__, file=sys.stderr)
        sys.exit(2)
    files = collect_md_files(sys.argv[1:])
    if not files:
        print("error: 未找到任何 .md 文件", file=sys.stderr)
        sys.exit(1)
    failed: list[str] = []
    for md_path in files:
        try:
            out = render_file(md_path)
        except Exception as exc:  # 单文件失败不中断批量
            failed.append(str(md_path))
            print(f"failed: {md_path} ({exc})", file=sys.stderr)
            continue
        print(f"rendered: {out}")
    if failed:
        print(f"error: {len(failed)} 个文件渲染失败: {', '.join(failed)}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
